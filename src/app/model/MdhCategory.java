package app.model;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author EngkoiZidac
 */
@Entity
@Table(name = "mdh_category")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "MdhCategory.findAll", query = "SELECT m FROM MdhCategory m")
    , @NamedQuery(name = "MdhCategory.findByCategoryId", query = "SELECT m FROM MdhCategory m WHERE m.categoryId = :categoryId")
    , @NamedQuery(name = "MdhCategory.findByDescription", query = "SELECT m FROM MdhCategory m WHERE m.description = :description")})
public class MdhCategory implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "CategoryId")
    private Integer categoryId;
    @Column(name = "Description")
    private String description;

    public MdhCategory() {
    }

    public MdhCategory(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public Integer getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (categoryId != null ? categoryId.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof MdhCategory)) {
            return false;
        }
        MdhCategory other = (MdhCategory) object;
        if ((this.categoryId == null && other.categoryId != null) || (this.categoryId != null && !this.categoryId.equals(other.categoryId))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "app.model.MdhCategory[ categoryId=" + categoryId + " ]";
    }
    
}
